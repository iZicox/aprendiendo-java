package ejerciciosBucles;

public class ejercicios_00 {

	public static void main(String[] args) {
		/*
		# Ejercicios de Bucles - De Básico a Avanzado

		## Nivel Básico - Bucles FOR

		1. **Conteo Simple**: Escribe un programa que imprima los números del 1 al 10.

		2. **Tabla de Multiplicar**: Crea un programa que muestre la tabla de multiplicar del 7.

		3. **Números Pares**: Imprime todos los números pares entre 1 y 50.

		4. **Cuenta Regresiva**: Realiza una cuenta regresiva desde 20 hasta 0.

		5. **Suma de N Números**: Pide al usuario un número N y calcula la suma de todos los números desde 1 hasta N usando un acumulador.

		
		## Nivel Básico - Bucles WHILE

		6. **Adivina el Número**: El programa tiene un número secreto (puedes definirlo tú). El usuario debe adivinarlo. El programa debe seguir pidiendo números hasta que acierte.

		7. **Validación de Contraseña**: Solicita al usuario que ingrese una contraseña. El programa debe seguir pidiéndola hasta que ingrese "secreto123".

		8. **Suma hasta Cero**: Pide números al usuario continuamente y ve sumándolos. El bucle termina cuando el usuario ingresa 0, momento en que se muestra la suma total.

		9. **Contador de Intentos**: Simula el ingreso a un sistema que solo permite 3 intentos de contraseña incorrecta. Usa un contador para llevar el registro.

		
		## Nivel Intermedio - Contadores y Acumuladores

		10. **Promedio de Calificaciones**: Pide al usuario que ingrese calificaciones una por una. Cuando ingrese -1, el programa termina y muestra el promedio. Usa un acumulador para la suma y un contador para la cantidad.

		11. **Números Positivos y Negativos**: Lee 15 números del usuario. Al final, muestra cuántos fueron positivos, cuántos negativos y cuántos ceros. Usa contadores independientes.

		12. **Factorial**: Calcula el factorial de un número N ingresado por el usuario (ejemplo: 5! = 5×4×3×2×1).

		13. **Mayor y Menor**: Pide 10 números al usuario y determina cuál es el mayor y cuál es el menor de todos.

		14. **Suma de Dígitos**: Dado un número entero, calcula la suma de todos sus dígitos (ejemplo: 1234 → 1+2+3+4 = 10).

		
		## Nivel Intermedio - Flags (Banderas)

		15. **Búsqueda de Número**: Genera 20 números aleatorios entre 1 y 100. Pide al usuario un número y verifica si está en la lista. Usa un flag para marcar si lo encontraste.

		16. **Número Primo**: Determina si un número ingresado por el usuario es primo o no. Usa un flag para marcar si encontraste algún divisor.

		17. **Secuencia Perfecta**: Pide números al usuario hasta que ingrese -1. Usa un flag para verificar si todos los números ingresados fueron pares. Al final, indica si la secuencia fue "perfecta" (todos pares) o no.

		18. **Validación Compleja**: Solicita un nombre de usuario que cumpla: mínimo 5 caracteres, máximo 15, y que contenga al menos un número. Usa flags para cada condición y sigue pidiendo hasta que todas se cumplan.

		
		## Nivel Avanzado - Combinaciones

		19. **Cajero Automático**: Simula un cajero con saldo inicial de 1000€. El usuario puede: 1) Consultar saldo, 2) Depositar, 3) Retirar, 4) Salir. Usa un contador de transacciones y un flag para controlar el menú. No permitir retirar más del saldo disponible.

		20. **Análisis de Ventas**: Pide el monto de ventas diarias hasta que se ingrese -1. Calcula: total de ventas (acumulador), promedio, cantidad de días con ventas superiores a 100€ (contador), y si hubo algún día sin ventas (flag para detectar ceros).

		21. **Juego de Dados**: Simula tirar dos dados. El jugador gana si saca 7 u 11 en la primera tirada, pierde si saca 2, 3 o 12. Cualquier otro número se convierte en su "punto" y debe seguir tirando hasta sacar su punto (gana) o sacar 7 (pierde). Usa flags para controlar el estado del juego y contadores para las tiradas.

		22. **Secuencia de Fibonacci con Límite**: Genera la secuencia de Fibonacci hasta que un número supere 1000. Cuenta cuántos números se generaron y usa un flag para detener cuando se alcance el límite.

		23. **Sistema de Calificaciones Completo**: Procesa calificaciones de estudiantes. Para cada estudiante, pide su nombre y 5 notas. Calcula su promedio. Continúa hasta que el usuario no quiera ingresar más estudiantes. Al final, muestra: total de estudiantes procesados, promedio general de todos, cuántos aprobaron (≥60) y si hubo algún estudiante con promedio perfecto (100). Usa múltiples contadores, acumuladores y flags.

		24. **Conversor de Temperatura con Menú**: Crea un menú que permita: 1) Celsius a Fahrenheit, 2) Fahrenheit a Celsius, 3) Ver historial de conversiones, 4) Salir. Usa contadores para el historial, acumuladores si quieres estadísticas, y flags para validar entradas y controlar el flujo del programa.

		25. **Simulador de Inversión**: El usuario invierte un capital inicial con un interés anual. Simula año por año hasta que el capital se duplique o pasen 30 años (lo que ocurra primero). Muestra cada año: año actual, capital acumulado, y interés ganado ese año. Usa flags para las condiciones de parada y acumuladores para el capital.
	*/
	}

}
